# print('That is Alice's cat.')
print("That is Alice's cat.")

# Escape Characters \' \" \t \n \\
print('That is Alice\'s cat.') 
print('Hasan says \"Hello World!\" in python.')

print("Hello There!\nHow are you?\n\tI\'m doing fine.")
print('add \\ sum') # Backslash

# Raw Strings: Inores all escapes characters. 
print(r'Hello There!\nHow are you?\n\tI\'m doing fine.')