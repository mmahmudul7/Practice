# Python Arithmetic Operators
# + Addition
# - Subtraction
# * Multiplication
# / Division
# % Modulus
# ** Exponentiation
# // Floor division
print("Arithmetic Operators")
a = 15
b = 2
print(a + b) # 17
print(a - b) # 13
print(a * b) # 30
print(a / b) # 7.5
print(a % b) # 1
print(2 ** 3) # 2 ^ 3 = 8
print(a // b) # 7
